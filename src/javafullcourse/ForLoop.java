package javafullcourse;
public class ForLoop {
    public static void main(String args[]){
        
        /*
        How for loop works
         steps
        1 initialize
        2 condition check
        3 increment or decrement
        4 excecute the code within loop
        
        */
        
        for(int i=0;i<10;i++){
            System.out.println(i);
        }
        
    }
}
